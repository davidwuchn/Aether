func TestIsAetherManagedOpenCodeDoc(t *testing.T) {
	tests := []struct {
		name    string
		content string
		want    bool
	}{
		{
			name:    "managed with sentinel",
			content: "# Colony\n\n> Aether Colony -- OpenCode Instructions\n\nSome content",
			want:    true,
		},
		{
			name:    "managed with generated marker",
			content: "# Colony\n\n*Generated by Aether setup. Customize this file for your project.*",
			want:    true,
		},
		{
			name:    "managed with enabled marker",
			content: "# Colony\n\nThis repo is Aether-enabled for OpenCode\n",
			want:    true,
		},
		{
			name:    "not managed — custom content",
			content: "# My Project\n\nCustom OpenCode instructions\n",
			want:    false,
		},
		{
			name:    "not managed — Codex doc",
			content: "# Colony\n\n> Aether Colony -- Codex CLI Instructions\n",
			want:    false,
		},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			if got := isAetherManagedOpenCodeDoc(tt.content); got != tt.want {
				t.Errorf("isAetherManagedOpenCodeDoc() = %v, want %v", got, tt.want)
			}
		})
	}
}

func TestPlatformRestartTargetsIncludesOpenCode(t *testing.T) {
	details := []map[string]interface{}{
		{"label": "AGENTS.md", "copied": 1},
		{"label": ".codex/CODEX.md", "copied": 1},
		{"label": ".opencode/OPENCODE.md", "copied": 1},
	}
	targets := platformRestartTargets(details)

	foundAGENTS := false
	foundOpenCode := false
	for _, t := range targets {
		if t == "AGENTS.md" {
			foundAGENTS = true
		}
		if t == ".opencode/OPENCODE.md" {
			foundOpenCode = true
		}
	}
	if !foundAGENTS {
		t.Error("expected AGENTS.md in restart targets")
	}
	if !foundOpenCode {
		t.Error("expected .opencode/OPENCODE.md in restart targets")
	}
}

func TestPlatformRestartMessageDualPlatform(t *testing.T) {
	targets := []string{"AGENTS.md", ".codex/CODEX.md", ".opencode/OPENCODE.md"}
	msg := platformRestartMessage(targets)

	if msg == "" {
		t.Fatal("expected non-empty restart message for dual-platform targets")
	}
	codexFound := false
	opencodeFound := false
	if containsStr(msg, "Close this Codex chat") {
		codexFound = true
	}
	if containsStr(msg, "Restart your OpenCode session") {
		opencodeFound = true
	}
	if !codexFound {
		t.Error("expected Codex restart guidance in dual-platform message")
	}
	if !opencodeFound {
		t.Error("expected OpenCode restart guidance in dual-platform message")
	}
}

